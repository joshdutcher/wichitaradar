            <h1>More specifically, I love the ways that technology can be used to help society.</h1>

            <p>We live in an amazing time. The Internet, wifi, and mobile data connect us all instantly and inexpensively while laptops, tablets, and phones allow us to walk around with incredibly powerful computers at our disposal. The combined computational power of today's society is just staggering, and the potential that can be realized by harnessing this resource is incredible. The possibility now exists, through software and connectivity, to improve the world dramatically, and not in a way that takes generations to realize. We can see these benefits in short order. For example, the digitizing of medical records by companies like Cerner and Epic Systems provides immediate benefit to almost everyone in the USA and hopefully soon, the world. The ability to effortlessly donate to organizations like the Red Cross in times of need is another stellar example. I love this kind of noble use of technology.</p>

            <h2>Ways to get it done: The "nudge".</h2>

            <blockquote class="twitter-tweet" lang="en" align="center"><p>&quot;Technology needs to make it so the right thing to do is the easiest thing to do&quot; <a href="https://twitter.com/farzad_onc">@Farzad_ONC</a> <a href="https://twitter.com/search?q=%23HIMSS13&amp;src=hash">#HIMSS13</a></p>&mdash; Chip Means (@chipmedia) <a href="https://twitter.com/chipmedia/statuses/309683958432550912">March 7, 2013</a></blockquote>

            <p>The idea of the "nudge" as presented by Richard Thaler and Cass Sunstein in their book <a href="http://amzn.com/014311526X">Nudge: Improving Decisions About Health, Wealth, and Happiness</a> deals with the fact that rather than carefully weighing each decision we make, we instead automatically make choices guided by experience, heuristics and context, and that by subtly changing those environmental cues, we as people can be "nudged" into better decisions - better for ourselves, better for society, etc. <a href="http://allthingsd.com/20130319/how-data-science-is-advancing-the-nudge-to-influence-mobile-behaviors/#">All Things D wrote a great article</a> covering the topic, and diving into the ways that mobile technology can be used to nudge people in ways that change behaviors, improve society, and even improve things like revenue, profits, and customer retention.</p>

            <h2>Another example: Crowdsourcing.</h2>

            <p>We've all heard the term "crowdsourcing", but not many of us have put as much thought into it as Thomas W. Malone, Robert Laubacher, and Chrysanthos Dellarocas. They are the authors of <a href="http://cci.mit.edu/publications/CCIwp2009-01.pdf">"Harnessing Crowds: Mapping the Genome of Collective Intelligence"</a> (pdf), a working paper from the MIT Center of Collective Intelligence. That's what they call crowdsourcing -- Collective Intelligence -- and it's a great way to think about it. It's more than just asking Twitter for an opinion. Foursquare crowdsourced their amazing database of businesses, creating a unique and invaluable asset. Local Motors became the first open source car company to reach production (<a href="http://www.wired.com/2010/01/ff_newrevolution/">here's a great article about that over at WIRED</a>). The MIT paper examines the phenomenon of collective intelligence in detail, providing a fascinating and useful framework by which companies who wish to crowdsource can effectively take advantage of the possibilities there. It's really an excellent read, I highly recommend it.</p>

            <h2>Who comes up with this stuff?</h2>

            <p>The short answer is: we do. Anyone with a new idea or an improvement on an existing one, now more than ever, is empowered to act on that idea, to test it in practice, to create and provide a new product, process, or service. It's called innovation, and the world is teeming with it, all because of technology. 3D printers and the rise of "Maker Spaces" are empowering the affordable manufacture of durable goods on a small scale - something that was not possible ten years ago. To cost-effectively manufacture goods used to require huge factories which could utilize economies of scale, requiring investment capital most folks didn't have access to. Today, technology has all but removed that barrier to entry. To crowdsource data, one needs only motivate smartphone users to provide that information. Achieving user adoption of an app is no small task, but anyone can become a mobile developer, or hire one to build their idea. Access to the computational power and, most importantly, the human power behind it, is as close as your nearest App Store. Again, technology has lowered or removed a barrier to entry in so many industries because we all carry little computers in our pockets. There's never been a better time in the history of humanity to turn an idea into reality.</p>

            <h2>Okay, but haven't all the good ideas been had already?</h2>

            <p>So maybe that quesiton wasn't on the tip of your tongue. Tough luck; this is my website and I asked it solely to give myself an excuse to post the video below. Steven Johnson is the author of <a href="http://amzn.com/1594487715">Where Good Ideas Come From: The Natural History of Innovation</a>. In that book he examines the environments that have historically spawned innovation, from the vast varieties of life present on coral reefs to the sparks of the Enlightenment which took place in English coffee houses when the people switched to coffee from drinking alcohol all day. Below is a TED talk in which he summarizes this idea, and I just think it's fascinating and inspirational. Have a look.</p>

